function note = make_note(n,fs,time,oct)
%Δημιουργία νότας με βάση τον πίνακα στη σελίδα 359 του βιβλίου.
% n = αριθμός στον αριθμητή του 220*2^(n/12)
% fs = συχνότητα δειγματοληψίας
% time = διάρκεια νότας
% oct = οκτάβα (θεωρούμε οκτάβα 1 αυτή με έυρος συχνοτήτων 220-440Hz

%Για την άσκηση Γ3

t = 0:1/fs:time;
f = 2^(oct-1)*220*2^(n/12);

note = sin(2*pi*f*t); %ημίτονο(2*π*f*t)